using System;


namespace MathOperations
{
    class Program
    {
        static void Main(string[] args)
        {
            int n1 = 3;
            int n2 = 7;
            int n3 = 6;

            // Adição
            Console.WriteLine(n3 + n2);

            // Subtração
            Console.WriteLine(n2 - n1);

            // Multiplicação
            Console.WriteLine(n3 * n2);

            // Divisão
            Console.WriteLine(n2 / n1);


        }

    }
}
